    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
//       refrigerado/con estantes,
//       refrigerado/sin estantes, 
//       no refrigerado/con estantes, 
//       no refrigerado/sin estantes) 

int refyEst = 100;
int refNoEst = 50;
int noRefEst = 40;
int noRefNoEst = 20;

DefaultPieDataset datos = new DefaultPieDataset();
    datos.setValue("Refrigerados con estante", refyEst);
    datos.setValue("Refrigerados sin estantes", refNoEst);
    datos.setValue("No refrigerados con estantes", noRefEst);
    datos.setValue("No refrigerados sin estantes", noRefNoEst);

    JFreeChart grafico = ChartFactory.createPieChart(
    "Depositos alquilados" + "total dep", //nombre del grafico
            datos, //set de datos
            true, //visibilidad categorias
            true, //herramientas
            false //generacion de URL
    );
    
    ChartPanel panel = new ChartPanel(grafico);
    
    panel1.setLayout(new BorderLayout());
    panel1.add(panel, BorderLayout.NORTH);
    
    pack();
    repaint();
    
    }//GEN-LAST:event_jButton1ActionPerformed

